}

get_build_jobs() {
    # Allow override via NPROC environment variable
    if [[ -n "${NPROC:-}" ]]; then
        echo "$NPROC"
        return
    fi
    
    local total_cpus
    total_cpus=$(nproc 2>/dev/null || sysctl -n hw.ncpu 2>/dev/null || echo "")
    
    if [[ -z "$total_cpus" ]]; then
        # Fallback when CPU count is unknown
        echo 4
    else
        # Use half of available CPUs, minimum 1
        local jobs=$(( total_cpus / 2 ))
        [[ $jobs -lt 1 ]] && jobs=1
        echo "$jobs"
    fi
}

# ---------------------------------------------------------
# Build System Detection and Compilation
# ---------------------------------------------------------

detect_build_system() {
    # Detect once per script run
    if [[ -n "$DETECTED_BUILD_SYSTEM" ]]; then
        return
    fi
    
    if [[ -f "go.mod" ]] || [[ -f "go.sum" ]] || [[ -f "src/go/go.mod" ]]; then
        DETECTED_BUILD_SYSTEM="go"
    elif [[ -f "Cargo.toml" ]]; then
        DETECTED_BUILD_SYSTEM="rust"
    elif [[ -f "autogen.sh" ]] || [[ -f "configure.ac" ]] || [[ -f "configure.in" ]]; then
        DETECTED_BUILD_SYSTEM="c-autotools"
    elif [[ -f "CMakeLists.txt" ]]; then
        DETECTED_BUILD_SYSTEM="c-cmake"
    elif [[ -f "meson.build" ]]; then
        DETECTED_BUILD_SYSTEM="c-meson"
    elif [[ -f "Makefile" ]] && ! [[ -f "configure" ]]; then
        DETECTED_BUILD_SYSTEM="c-make"
    else
        DETECTED_BUILD_SYSTEM="none"
    fi
    
    log "Detected build system: $DETECTED_BUILD_SYSTEM"
}

build_project() {
    detect_build_system
    
    case "$DETECTED_BUILD_SYSTEM" in
        go|rust|none)
            # Go compiles on test run, Rust handled by cargo test, none = nothing to do
            return 0
            ;;
        c-autotools)
            build_autotools
            ;;
        c-cmake)
            build_cmake
            ;;
        c-meson)
            build_meson
            ;;
        c-make)
            build_make
            ;;
    esac
}

build_autotools() {
    local nproc_val
    nproc_val=$(get_build_jobs)
    
    if [[ ! -f configure ]]; then
        if [[ -f autogen.sh ]]; then
            log "Running autogen.sh..."
            bash autogen.sh > "${ARTIFACT_DIR}/autogen.log" 2>&1 || {
                log "ERROR: autogen.sh failed (see ${ARTIFACT_DIR}/autogen.log)"
                exit 1
            }
        elif [[ -f configure.ac ]]; then
            log "Running autoreconf..."
            autoreconf -fi > "${ARTIFACT_DIR}/autoreconf.log" 2>&1 || {
                log "ERROR: autoreconf failed (see ${ARTIFACT_DIR}/autoreconf.log)"
                exit 1
            }
        fi
    fi

    # Partial trees often keep ./configure and Makefile but omit Automake's helper
    # scripts (e.g. ./missing). make clean can then try to remake aclocal.m4 and
    # fail with: /bin/bash: .../missing: No such file or directory.
    if [[ -f configure ]] && [[ ! -f missing ]]; then
        local makefile_refs_missing=false
        if [[ -f Makefile ]] && grep -qF 'missing aclocal' Makefile 2>/dev/null; then
            makefile_refs_missing=true
        elif [[ -f Makefile.in ]] && grep -qF 'missing aclocal' Makefile.in 2>/dev/null; then
            makefile_refs_missing=true
        fi
        if [[ "$makefile_refs_missing" == true ]]; then
            if [[ -f autogen.sh ]]; then
                log "Running autogen.sh (restoring autotools helpers)..."
                bash autogen.sh > "${ARTIFACT_DIR}/autogen_helpers.log" 2>&1 || {
                    log "ERROR: autogen.sh failed (see ${ARTIFACT_DIR}/autogen_helpers.log)"
                    exit 1
                }
            elif [[ -f configure.ac ]]; then
                log "Running autoreconf -fi (restoring autotools helpers)..."
                autoreconf -fi > "${ARTIFACT_DIR}/autoreconf_helpers.log" 2>&1 || {
                    log "ERROR: autoreconf failed (see ${ARTIFACT_DIR}/autoreconf_helpers.log)"
                    exit 1
                }
            fi
        fi
    fi
    
    # Suricata-specific workaround: If libhtp/ exists but is missing source
    # files (only build artifacts from container mount), fall back to system
    # libhtp. This is a TEMPORARY fix - proper solution is at container level.
    local extra_flags=""
    local is_suricata=false
    if [[ -f "suricata.yaml.in" ]] || [[ -f "src/suricata.c" ]]; then
        is_suricata=true
    fi

    if [[ "$is_suricata" == true ]]; then
        ensure_pcre2_for_suricata_build
    fi
    
    if [[ "$is_suricata" == true ]] && [[ -d libhtp ]] && [[ ! -f libhtp/htp/htp.c ]]; then
        if pkg-config --exists htp 2>/dev/null; then
            log "WARNING: Bundled libhtp missing source files; using system libhtp ($(pkg-config --modversion htp))"
            log "WARNING: This is a temporary workaround - may cause runtime incompatibility"
            extra_flags="--enable-non-bundled-htp"
            rm -f Makefile
        else
            log "WARNING: Bundled libhtp is incomplete and no system libhtp found"
        fi
    fi
    
    if [[ -f configure ]] && [[ ! -f Makefile ]]; then
        log "Running ./configure ${CONFIGURE_FLAGS} ${extra_flags}..."
        ./configure ${CONFIGURE_FLAGS} ${extra_flags} > "${ARTIFACT_DIR}/configure.log" 2>&1 || {
            log "ERROR: configure failed (see ${ARTIFACT_DIR}/configure.log)"
            exit 1
        }
    fi

    # Suricata: full clean before every build. Incremental make across base/mutation
    # checkouts can leave stale .o/.a linked with a different tree and cause segfaults in tests.
    if [[ "$is_suricata" == true ]] && [[ -f Makefile ]]; then
        log "Suricata: running make clean before build..."
        make clean > "${ARTIFACT_DIR}/make_clean.log" 2>&1 || {
            log "ERROR: make clean failed (see ${ARTIFACT_DIR}/make_clean.log)"
            exit 1
        }
    fi

    # After make clean (fresh objects); before compile. Idempotent if already patched.
    if [[ "$is_suricata" == true ]]; then
        ensure_suricata_siocgstamp_workaround
    fi
    
    log "Running make -j${nproc_val}..."
    make -j"${nproc_val}" > "${ARTIFACT_DIR}/make.log" 2>&1 || {
        log "ERROR: make failed (see ${ARTIFACT_DIR}/make.log)"
        exit 1
    }
    
    log "Build complete."
}

