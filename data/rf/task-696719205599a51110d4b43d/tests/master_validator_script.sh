clean_workspace() {
    log "Cleaning workspace..."
    git reset --hard > /dev/null || { log "ERROR: git reset --hard failed"; exit 1; }
    
    # Exclude mounted/special files that cannot be removed in container environments
    local exclude_opts=()
    for f in run_master_validator.sh master_validator_script.sh run_script.sh \
             compare_test_runs.py parsing_script.py before_repo_set_cmd.sh build_script.sh schemas/; do
        [[ -e "$f" ]] && exclude_opts+=(-e "$f")
    done
    
    # Add build artifact exclusions based on detected build system
    detect_build_system
    case "$DETECTED_BUILD_SYSTEM" in
        c-autotools)
            # Preserve autotools artifacts for incremental builds
            for pattern in '*.o' '.deps' '.libs' 'Makefile' 'Makefile.in' \
                          'config.*' 'aclocal.m4' 'autom4te.cache' 'compile' \
                          'depcomp' 'install-sh' 'ltmain.sh' 'missing' 'libtool' \
                          'configure' 'stamp-*'; do
                exclude_opts+=(-e "$pattern")
            done
            ;;
        c-cmake)
            exclude_opts+=(-e "build")
            ;;
        c-meson)
            exclude_opts+=(-e "builddir")
            ;;
        c-make)
            for pattern in '*.o' '*.a' '*.so' '*.dylib'; do
                exclude_opts+=(-e "$pattern")
            done
            ;;
    esac
    
    if ! git clean -fd "${exclude_opts[@]}" 2>&1; then
        log "WARNING: git clean encountered errors (some files may not be removed)"
    fi
}
 
checkout_commit() {
