copy_artifact_with_fallback() {
    local destination="$1"
    shift
    local candidates=("$@")
    local candidate
 
    for candidate in "${candidates[@]}"; do
        if [[ -f "${ARTIFACT_DIR}/${candidate}" ]]; then
            cp "${ARTIFACT_DIR}/${candidate}" "$destination"
            return 0
        fi
    done
 
    return 1
}
 
create_delivery_zip() {
    if [[ -z "${TASK_ID:-}" ]]; then
        log "ERROR: Could not create delivery zip because TASK_ID is missing."
        return 1
    fi
 
    local zip_root="${ARTIFACT_DIR}/${TASK_ID}_v3.1"
    local zip_output="${ARTIFACT_DIR}/${TASK_ID}_v3.1.zip"
    local missing=()
 
    rm -rf "$zip_root"
    mkdir -p "${zip_root}/reference" "${zip_root}/agent" "${zip_root}/e2e" "${zip_root}/scripts"
 
    # -------------------------------------------------------------------------
    # reference/ files (14 files)
    # -------------------------------------------------------------------------
    # (1) reference.patch - Auto-generated correct solution (from PR; git diff base commit instance commit)
    copy_artifact_with_fallback "${zip_root}/reference/reference.patch" "reference.patch" || missing+=("reference/reference.patch (expected: reference.patch)")
