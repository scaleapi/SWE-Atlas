        "reference_ratings.png:reference/reference_ratings.png"                  # (14)
    )
    
    # Agent mode files (7 files)
    local agent_files=(
        "environment.png:agent/environment.png"                                  # (15)
        "conversation.md:agent/conversation.md"                                  # (16)
        "agent.patch:agent/agent.patch"                                          # (17)
        "agent_comparison_results.json:agent/comparison_results.json"            # (18)
        "agent_success.txt:agent/success.txt"                                    # (19)
        "test_failure_justification.txt:agent/test_failure_justification.txt"    # (20)
        "rubric_failure_justification.txt:agent/rubric_failure_justification.txt"  # (21)
    )
    
    log "Checking Reference mode files..."
    for spec in "${ref_files[@]}"; do
        local zip_path="${spec##*:}"
        local candidates="${spec%%:*}"
        local found=false
        
        IFS='|' read -ra files <<< "$candidates"
        for file in "${files[@]}"; do
            if [[ -f "${ARTIFACT_DIR}/${file}" ]]; then
                present+=("✓ ${zip_path} (found: ${file})")
                found=true
                break
            fi
        done
        if [[ "$found" == "false" ]]; then
            missing+=("✗ ${zip_path} (expected: ${candidates})")
        fi
    done
    
    log "Checking Agent mode files..."
    for spec in "${agent_files[@]}"; do
        local zip_path="${spec##*:}"
        local candidates="${spec%%:*}"
        local found=false
        
        IFS='|' read -ra files <<< "$candidates"
        for file in "${files[@]}"; do
            if [[ -f "${ARTIFACT_DIR}/${file}" ]]; then
                present+=("✓ ${zip_path} (found: ${file})")
                found=true
                break
            fi
        done
        if [[ "$found" == "false" ]]; then
            missing+=("✗ ${zip_path} (expected: ${candidates})")
        fi
    done
    
    echo ""
    log "--- FILES PRESENT ---"
    for item in "${present[@]}"; do
        log "  ${item}"
    done
    
    if [[ ${#missing[@]} -gt 0 ]]; then
        echo ""
        log "--- FILES MISSING ---"
        for item in "${missing[@]}"; do
            log "  ${item}"
        done
        echo ""
        log "WARNING: ${#missing[@]} file(s) missing for E2E zip creation."
        log "         The zip cannot be created until all files are present."
    else
        echo ""
        log "SUCCESS: All files present for E2E zip creation."
    fi
    
    log "=========================================="
}

