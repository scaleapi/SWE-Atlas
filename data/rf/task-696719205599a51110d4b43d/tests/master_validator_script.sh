# ---------------------------------------------------------
# Argument Parsing
# ---------------------------------------------------------

parse_args() {
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --mode)
                shift
                case "$1" in
                    reference|ref)
                        MODE="reference"
                        ;;
                    agent)
                        MODE="agent"
                        ;;
                    e2e)
                        MODE="e2e"
                        ;;
                    instance_validation|iv)
                        MODE="instance_validation"
                        ;;
                    verify|vf)
                        MODE="verify"
                        ;;
                    *)
                        log "ERROR: Invalid mode '$1'. Valid modes: reference, ref, agent, e2e, instance_validation, iv, verify, vf"
                        exit 1
                        ;;
                esac
                shift
                ;;
            --relax|--full-suite)
                RELAX_MODE=true
                shift
                ;;
            -h|--help)
                usage
                ;;
            *)
                log "ERROR: Unknown option '$1'"
                usage
                ;;
        esac
    done
}

