ensure_zip_available() {
    if command -v zip &>/dev/null; then
        return 0
    fi

    log "WARNING: 'zip' command not found. Attempting to install..."

    if command -v apt-get &>/dev/null; then
        if apt-get update -qq && apt-get install -y -qq zip 2>/dev/null; then
            log "SUCCESS: Installed 'zip' via apt-get"
            return 0
        fi
    fi

    if command -v apk &>/dev/null; then
        if apk add --quiet zip 2>/dev/null; then
            log "SUCCESS: Installed 'zip' via apk"
            return 0
        fi
    fi

    log "WARNING: Could not install 'zip'. The zip command is not available and could not be installed automatically."
    log "         Please install it manually (apt-get install zip / apk add zip) to create delivery zips."
    return 1
}

# Suricata: ./configure (and make clean → config.status --recheck) require libpcre2.
ensure_pcre2_for_suricata_build() {
    if command -v pkg-config &>/dev/null && pkg-config --exists libpcre2-8 2>/dev/null; then
        return 0
    fi
    if [[ -f /usr/include/pcre2.h ]] || [[ -f /usr/local/include/pcre2.h ]]; then
        return 0
    fi

    log "WARNING: PCRE2 development files not found; installing for Suricata build..."

    if command -v apt-get &>/dev/null; then
        if apt-get update -qq && apt-get install -y -qq libpcre2-dev 2>/dev/null; then
            log "SUCCESS: Installed libpcre2-dev via apt-get"
            return 0
        fi
    fi

    if command -v dnf &>/dev/null; then
        if dnf install -y -q pcre2-devel 2>/dev/null; then
            log "SUCCESS: Installed pcre2-devel via dnf"
            return 0
        fi
    fi

    if command -v yum &>/dev/null; then
        if yum install -y -q pcre2-devel 2>/dev/null; then
            log "SUCCESS: Installed pcre2-devel via yum"
            return 0
        fi
    fi

    if command -v apk &>/dev/null; then
        if apk add --quiet pcre2-dev 2>/dev/null; then
            log "SUCCESS: Installed pcre2-dev via apk"
            return 0
        fi
    fi

    if command -v pacman &>/dev/null; then
        if pacman -Sy --noconfirm pcre2 2>/dev/null; then
            log "SUCCESS: Installed pcre2 via pacman"
            return 0
        fi
    fi

    log "ERROR: Could not install PCRE2 development files (e.g. libpcre2-dev, pcre2-devel). Install them in the image and retry."
    exit 1
}

# glibc 2.31+ userspace: the traditional timeval ioctl was renamed to SIOCGSTAMP_OLD.
# Older Suricata sources still call ioctl(..., SIOCGSTAMP, ...), which fails to compile
# with "'SIOCGSTAMP' undeclared". Patching the one call site avoids editing Makefile
# CPPFLAGS (the generated Makefile sets CPPFLAGS= and overrides the environment).
ensure_suricata_siocgstamp_workaround() {
    local f="src/source-af-packet.c"
    [[ -f "$f" ]] || return 0
    if grep -qF 'ioctl(ptv->socket, SIOCGSTAMP,' "$f" 2>/dev/null; then
        log "Suricata: applying SIOCGSTAMP → SIOCGSTAMP_OLD workaround for ${f} (glibc 2.31+)"
        sed -i 's/ioctl(ptv->socket, SIOCGSTAMP,/ioctl(ptv->socket, SIOCGSTAMP_OLD,/' "$f"
    fi
}

