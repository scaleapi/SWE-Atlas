_progress_monitor() {
    local log_file="$1"
    local start_time="$2"
    local interval="${3:-10}"

    trap 'exit 0' TERM

    while true; do
        sleep "$interval"
        local now
        now=$(date +%s)
        local elapsed=$((now - start_time))

        local passed=0 failed=0 errors=0
        if [[ -s "$log_file" ]]; then
            read -r passed failed errors <<< "$(awk '
                /PASSED|passed/ { p++ }
                /FAILED|failed/ { f++ }
                /ERROR|error/   { e++ }
                END { print p+0, f+0, e+0 }
            ' "$log_file" 2>/dev/null)" || true
        fi

        local total=$((passed + failed + errors))
        log "  ♥ [${elapsed}s] ${total} done (${passed} passed, ${failed} failed, ${errors} errors)" >&2
    done
}

run_tests_and_parse() {
    local output_prefix="$1"
    local test_list_file="${2:-}"  # Optional: JSON file with test files to run
    local stdout_log="${output_prefix}_stdout.log"
    local stderr_log="${output_prefix}_stderr.log"
    local output_json="${output_prefix}_results.json"
    local start_time=$(date +%s)
    
    # Optional: restrict run_script to specific test files (master validator does not pass this)
    local test_files_args=()
    if [[ -n "$test_list_file" && -f "$test_list_file" ]]; then
        log "Reading test file list from ${test_list_file}..." >&2
        # Extract file names from JSON: {"files": [{"name": "path/to/test.py"}, ...]}
        test_files_args=($(python3 -c "import json; data=json.load(open('$test_list_file')); print(' '.join([f['name'] for f in data.get('files', [])]))" 2>/dev/null)) || true
        if [[ ${#test_files_args[@]} -gt 0 ]]; then
            log "Running ${#test_files_args[@]} specific test file(s)" >&2
        else
            log "WARNING: No test files found in ${test_list_file}, running all tests" >&2
        fi
    else
        log "Running full test suite (all tests)" >&2
    fi
    
    log "Running tests (logs: ${stdout_log})..." >&2

    # Uses direct file redirection instead of process substitution (> >(...))
    # to avoid orphaned tee/awk subshells that can deadlock the script.
    # A background monitor polls the log file for progress + heartbeat.
    : > "$stdout_log"
    : > "$stderr_log"

    _progress_monitor "$stdout_log" "$start_time" 10 &
    local monitor_pid=$!

    if [[ ${#test_files_args[@]} -gt 0 ]]; then
        PYTHONUNBUFFERED=1 bash "$RUN_SCRIPT" "$output_json" "${test_files_args[@]}" \
            </dev/null >"$stdout_log" 2>"$stderr_log" || true
    else
        PYTHONUNBUFFERED=1 bash "$RUN_SCRIPT" "$output_json" \
            </dev/null >"$stdout_log" 2>"$stderr_log" || true
    fi

    kill "$monitor_pid" 2>/dev/null
    wait "$monitor_pid" 2>/dev/null || true

    local end_time=$(date +%s)
    local elapsed=$((end_time - start_time))

    # Final progress summary
    local passed=0 failed=0 errors=0
    if [[ -s "$stdout_log" ]]; then
        read -r passed failed errors <<< "$(awk '
            /PASSED|passed/ { p++ }
            /FAILED|failed/ { f++ }
            /ERROR|error/   { e++ }
            END { print p+0, f+0, e+0 }
        ' "$stdout_log" 2>/dev/null)" || true
    fi
    local total=$((passed + failed + errors))
    log "Tests finished in ${elapsed}s — ${total} done (${passed} passed, ${failed} failed, ${errors} errors)" >&2

    # The run_script writes JSON directly to output_json
    # Verify the output file exists and is valid JSON
    if [[ ! -f "$output_json" ]]; then
        log "ERROR: Run script did not create output file: $output_json" >&2
        # Create a minimal error result
        cat > "$output_json" << EOF
{
  "passed_test_count": 0,
  "failed_test_count": 0,
  "skipped_test_count": 0,
  "passed_tests": [],
  "failed_tests": [],
  "exit_code": 1,
  "error": "Run script did not produce output"
}
EOF
    fi
    
    log "Results saved to: $output_json" >&2
    # Return only the path (stdout is captured by caller)
    echo "$output_json"
}

# In strict mode: each listed relevant name must match at least one executed test name
# in baseline and/or mutation run JSON (passed, failed, skipped, or legacy tests[]).
validate_relevant_tests_in_run_outputs() {
    local baseline_json="$1"
    local mutation_json="$2"
    local relevant_names_json="$3"
    local label="$4"

    python3 - "$baseline_json" "$mutation_json" "$relevant_names_json" "$label" << 'PYEOF'
import json
import sys

baseline_json, mutation_json, relevant_names_json, label = sys.argv[1], sys.argv[2], sys.argv[3], sys.argv[4]

# --- validate_relevant_tests_in_run_outputs (inline) ---
# Ensures every defined "relevant" test name from relevant_test_names.json actually
# showed up in the test harness output for at least one of baseline or mutation runs.
# Exit 0 if every listed name matches some executed test string; exit 1 otherwise.
# Matching is fuzzy because runners emit full IDs (e.g. pytest file::Class::method) while
# the JSON often lists shorter canonical names.

def matches_test_name(full_name, relevant_names):
    """Return True if full_name (from runner output) should count as one of relevant_names."""
    if full_name in relevant_names:
        return True
    # Jest-style "path | test name"
    if ' | ' in full_name:
        test_name = full_name.split(' | ', 1)[1].strip()
        if test_name in relevant_names:
            return True
        for rn in relevant_names:
            if rn.endswith(test_name):
                return True
    # pytest-style path::Class::method
    if '::' in full_name:
        method_name = full_name.split('::')[-1]
        if method_name in relevant_names:
            return True
        parts = full_name.split('::')
        if len(parts) >= 2:
            class_method = '::'.join(parts[-2:])
            if class_method in relevant_names:
                return True
    # Go subtests: TestFoo/Subtest
    if '/' in full_name:
        parent_name = full_name.split('/')[0]
        if parent_name in relevant_names:
            return True
    # Last resort: substring overlap between listing and runner string
    for rn in relevant_names:
        if rn in full_name or full_name in rn:
            return True
    return False

def collect_executed_names(data):
    """Flatten one run_script JSON into every test name string that was reported."""
    names = []
    # Legacy schema: single list with status per test
    if 'tests' in data:
        for t in data.get('tests', []) or []:
            n = t.get('name', '')
            if n:
                names.append(n)
    # Current schema: separate buckets (include skipped — test was executed/discovered)
    for key in ('passed_tests', 'failed_tests', 'skipped_tests'):
        for item in data.get(key, []) or []:
            if isinstance(item, dict):
                n = item.get('name', '')
            else:
                n = str(item)
            if n:
                names.append(n)
    return names

with open(baseline_json) as f:
    baseline = json.load(f)
with open(mutation_json) as f:
    mutation = json.load(f)
with open(relevant_names_json) as f:
    rel = json.load(f)
    # Expected shape: {"tests": [{"name": "..."}, ...]}
    relevant_list = [t['name'] for t in rel.get('tests', []) if t.get('name')]

# Union of names from both runs (e.g. new test only in mutation still satisfies "ran")
executed = collect_executed_names(baseline) + collect_executed_names(mutation)
missing = []
for rn in relevant_list:
    # {rn} is the only member of the set: matches_test_name expects a set of candidate labels
    if not any(matches_test_name(cn, {rn}) for cn in executed):
        missing.append(rn)

if missing:
    # stderr only: keep stdout clean for any caller parsing "OK:" lines
    print(
        f"ERROR: {len(missing)} test(s) from {label} were not found in baseline or mutation run output "
        f"(check {baseline_json} / {mutation_json}):",
        file=sys.stderr,
    )
    for m in missing[:50]:
        print(f"  - {m}", file=sys.stderr)
    if len(missing) > 50:
        print(f"  ... and {len(missing) - 50} more", file=sys.stderr)
    sys.exit(1)

print(f"OK: All {len(relevant_list)} test(s) from {label} appear in baseline and/or mutation run output.")
PYEOF
}

# In strict mode, every name in relevant_test_names.json must appear verbatim in the
# comparison output (exact string match against the union of transition buckets).
validate_relevant_tests_in_comparison() {
    local comparison_json="$1"
    local relevant_names_json="$2"
    local label="${3:-relevant}"

    python3 - "$comparison_json" "$relevant_names_json" "$label" << 'PYEOF'
import json
import sys

comparison_json, relevant_names_json, label = sys.argv[1], sys.argv[2], sys.argv[3]

# --- validate_relevant_tests_in_comparison (inline) ---
# After compare_test_runs.py, each real test appears in exactly one transition list (P2P, F2F, …).
# Every name in relevant_test_names.json must appear as an exact string in that union
# (relevant_test_names is expected to be sourced from the same comparison artifact).
# Exit 1 if any listed name is absent from the diff (would mean "lost" from compare output).

def collect_comparison_test_names(obj):
    """Gather every test identifier appearing in any transition bucket of the comparison JSON."""
    keys = (
        'pass_to_pass', 'pass_to_fail', 'pass_to_missing',
        'fail_to_pass', 'fail_to_fail', 'fail_to_missing',
        'missing_to_pass', 'missing_to_fail',
    )
    names = []
    for k in keys:
        for item in obj.get(k, []) or []:
            if isinstance(item, str):
                names.append(item)
            elif isinstance(item, dict) and 'name' in item:
                names.append(item['name'])
    return names

with open(comparison_json) as f:
    comparison = json.load(f)
with open(relevant_names_json) as f:
    rel = json.load(f)
    relevant_list = [t['name'] for t in rel.get('tests', []) if t.get('name')]

comparison_set = set(collect_comparison_test_names(comparison))
missing = []
for rn in relevant_list:
    if rn not in comparison_set:
        missing.append(rn)

if missing:
    # Listed name not present verbatim in the transition union → not in compare_test_runs output
    print(f"ERROR: {len(missing)} test(s) from {label} list are absent from comparison results ({comparison_json}):", file=sys.stderr)
    for m in missing[:50]:
        print(f"  - {m}", file=sys.stderr)
    if len(missing) > 50:
        print(f"  ... and {len(missing) - 50} more", file=sys.stderr)
    print("Each relevant test must appear in the comparison under a transition (P2P, F2F, F2P, etc.).", file=sys.stderr)
    sys.exit(1)

print(f"OK: All {len(relevant_list)} test(s) from {label} are present in comparison results.")
PYEOF
}

# Strict mode: no regressions (P2F) or new failing tests (M2F) anywhere in the comparison.
validate_strict_forbidden_transitions() {
    local comparison_json="$1"

    python3 - "$comparison_json" << 'PYEOF'
import json
import sys

# --- validate_strict_forbidden_transitions (inline) ---
# compare_test_runs.py classifies each test into transition buckets. Strict policy:
#   - P2F (pass_to_fail): was passing at baseline, fails after mutation → regression.
#   - M2F (missing_to_fail): absent at baseline, fails after mutation → new failing test.
# Any non-empty pass_to_fail or missing_to_fail fails validation (whole suite, not only
# names from relevant_test_names.json). Relax mode skips this script entirely.

path = sys.argv[1]
with open(path) as f:
    c = json.load(f)

def as_names(items):
    """Normalize bucket entries to strings (compare JSON may use str or {"name": ...})."""
    out = []
    for item in items or []:
        if isinstance(item, str):
            out.append(item)
        elif isinstance(item, dict) and item.get("name"):
            out.append(item["name"])
    return out

p2f = as_names(c.get("pass_to_fail"))
m2f = as_names(c.get("missing_to_fail"))

# Either bucket having entries means at least one test violated strict quality gates
if p2f or m2f:
    print("ERROR: Strict mode does not allow P2F (pass→fail regressions) or M2F (new failing tests).", file=sys.stderr)
    print(f"  Comparison file: {path}", file=sys.stderr)
    if p2f:
        print(f"  P2F ({len(p2f)}):", file=sys.stderr)
        for t in p2f[:30]:
            print(f"    - {t}", file=sys.stderr)
        if len(p2f) > 30:
            print(f"    ... and {len(p2f) - 30} more", file=sys.stderr)
    if m2f:
        print(f"  M2F ({len(m2f)}):", file=sys.stderr)
        for t in m2f[:30]:
            print(f"    - {t}", file=sys.stderr)
        if len(m2f) > 30:
            print(f"    ... and {len(m2f) - 30} more", file=sys.stderr)
    sys.exit(1)

# Success path: comparison exists and has no forbidden buckets
print("OK: Strict mode — no P2F or M2F transitions in comparison.")
PYEOF
}
 
