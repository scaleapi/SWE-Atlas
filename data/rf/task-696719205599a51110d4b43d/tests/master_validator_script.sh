#!/bin/bash
 
set -e
 
# ==============================================================================
# Master Validator Script - Refactor Project
# ==============================================================================
# Validates refactor artifacts in multiple modes:
#
# REFERENCE MODE (--mode reference|ref):
#   Baseline is always: checkout at base commit
#   Mutation is one of:
#     a) Patch    - apply gold.patch + test.patch on base commit [default]
#     b) Commit   - checkout at instance commit
#
# AGENT MODE (--mode agent):
#   - Baseline: checkout at base commit
#   - Mutation: checkout at base commit + apply agent patch + test.patch
#   - Patches: agent.patch + test.patch
#
# E2E MODE (--mode e2e):
#   - Runs Reference mode, then Agent mode, then creates delivery zip
#
# INSTANCE VALIDATION MODE (--mode instance_validation|iv):
#   - Baseline: checkout at base commit
#   - Mutation: checkout at base commit + apply reference.patch
#   - Validates that the instance commit changes don't introduce regressions
#   - Uses reference.patch generated by before_repo_set_cmd.sh
#
# VERIFY MODE (--mode verify|vf):
#   - Checks if all required files exist for E2E zip creation
#   - Also verifies that the 'zip' command is available
#
# ==============================================================================
# Test Execution & Validation Modes
# ==============================================================================
#
# Test runs are ALWAYS the full suite.
#
# STRICT MODE (default, when --relax is NOT passed):
#   - Requires (reference and agent both): relevant_file_names.json + relevant_test_names.json
#     → ERROR if either path is missing.
#   - Every name in relevant_test_names.json must appear in baseline and/or mutation run output
#     → ERROR if any listed name is missing from executed results.
#   - Every listed name must appear in the comparison JSON (some transition bucket)
#     → ERROR if any listed name is absent from the comparison artifact.
#   - Forbidden transitions (entire comparison, not only listed names): any P2F or M2F
#     → ERROR if pass_to_fail or missing_to_fail is non-empty.
#
# RELAX MODE (--relax):
#   - Same full test-suite execution and full baseline vs mutation comparison.
#   - Skips the relevant-file requirement, run/comparison coverage checks, and P2F/M2F failure.
#
# ==============================================================================
# Test Transition Behavior (P=Pass, F=Fail, M=Missing)
# ==============================================================================
#
# +------------+------------------+------------------+--------------------------------------------+
# | Transition | Baseline         | Mutation         | Meaning                                    |
# +------------+------------------+------------------+--------------------------------------------+
# | P2P        | PASS             | PASS             | Stable passing                             |
# | F2F        | FAIL             | FAIL             | Stable failing                             |
# | F2P        | FAIL             | PASS             | Improvement                                |
# | P2F        | PASS             | FAIL             | Regression (!)                            |
# | M2P        | MISSING          | PASS             | New test added, passes                     |
# | M2F        | MISSING          | FAIL             | New test added, fails (!)                 |
# | P2M        | PASS             | MISSING          | Passing test removed                       |
# | F2M        | FAIL             | MISSING          | Failing test removed                       |
# +------------+------------------+------------------+--------------------------------------------+
#
# ==============================================================================
 
