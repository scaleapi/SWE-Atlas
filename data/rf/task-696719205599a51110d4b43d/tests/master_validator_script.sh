# Strict mode: both relevant JSON files must exist (shared by reference and agent).
require_strict_relevant_artifacts() {
    local mode_label="$1"
    if [[ ! -f "$REF_TEST_LIST" || ! -f "$REF_TEST_NAMES" ]]; then
        log "ERROR: Strict (${mode_label}) mode requires both:"
        log "  - $REF_TEST_LIST"
        log "  - $REF_TEST_NAMES"
        exit 1
    fi
}

run_reference_mode() {
    # ---------------------------------------------------------
    # Ensure artifact directory exists
    # ---------------------------------------------------------
    
    mkdir -p "$ARTIFACT_DIR"

    log "========== REFERENCE MODE =========="
    echo ""

    # Strict mode unless --relax
    STRICT_MODE=false
    if [[ "$RELAX_MODE" == "true" ]]; then
        log "Relax mode (--relax): skipping strict relevant-file checks and run/comparison list validation."
    else
        require_strict_relevant_artifacts "reference"
        STRICT_MODE=true
        log "Strict mode: full test suite; validate relevant_test_names.json against run output and comparison."
    fi
    echo ""

    # Check required files
    log "Checking required scripts..."
    check_files_exist "$RUN_SCRIPT" "$COMPARE_SCRIPT" "$TEST_PATCH" "$FILTERED_REF_PATCH"
    log "SUCCESS: All files present."
    echo ""

    # ---------------------------------------------------------
    # Baseline: checkout at base commit
    # ---------------------------------------------------------
    log "=== Running BASELINE tests (base commit) ==="
    clean_workspace
    checkout_commit "$BASE_COMMIT" "base commit"
    build_project
    
    BASELINE_JSON=$(run_tests_and_parse "${ARTIFACT_DIR}/ref_baseline")
    log "SUCCESS: Baseline complete."
    echo ""

    # ---------------------------------------------------------
    # Mutation
    # ---------------------------------------------------------
    log "=== Running MUTATION tests (base commit + patches) ==="
    clean_workspace
    checkout_commit "$BASE_COMMIT" "base commit"
    
    log "Applying gold.patch..."
    if ! git apply "$FILTERED_REF_PATCH"; then
        log "ERROR: Failed to apply gold.patch"
        exit 1
    fi
    log "SUCCESS: Applied gold.patch"
    
    log "Applying test.patch..."
    if ! git apply "$TEST_PATCH"; then
        log "ERROR: Failed to apply test.patch"
        exit 1
    fi
    log "SUCCESS: Applied test.patch"

    build_project
    
    MUTATION_JSON=$(run_tests_and_parse "${ARTIFACT_DIR}/ref_mutation")
    log "SUCCESS: Mutation complete."
    echo ""

    if [[ "$STRICT_MODE" == "true" ]]; then
        log "Validating relevant tests appeared in baseline and/or mutation run output..."
        validate_relevant_tests_in_run_outputs "$BASELINE_JSON" "$MUTATION_JSON" "$REF_TEST_NAMES" "relevant_test_names.json"
    fi

    # ---------------------------------------------------------
    # Compare results
    # ---------------------------------------------------------
    log "=== Comparing results ==="
    COMPARISON_JSON="${ARTIFACT_DIR}/ref_comparison_results.json"
    python3 "$COMPARE_SCRIPT" "$BASELINE_JSON" "$MUTATION_JSON" "$COMPARISON_JSON"
    
    display_comparison "$COMPARISON_JSON"

    if [[ "$STRICT_MODE" == "true" ]]; then
        log "Validating relevant tests are represented in comparison (transitions)..."
        validate_relevant_tests_in_comparison "$COMPARISON_JSON" "$REF_TEST_NAMES" "relevant_test_names.json"
        log "Validating strict mode forbids P2F and M2F transitions..."
        validate_strict_forbidden_transitions "$COMPARISON_JSON"
    fi
    
    log "Results saved to:"
    log "  Baseline: $BASELINE_JSON"
    log "  Mutation: $MUTATION_JSON"
    log "  Comparison: $COMPARISON_JSON"
    if [[ "$STRICT_MODE" == "true" ]]; then
        log "  (Strict mode: relevant coverage + no P2F/M2F)"
    fi
    echo ""

    create_success_file "reference"

    # Cleanup
    clean_workspace
    checkout_commit "$BASE_COMMIT" "base commit (cleanup)"
    log "========== REFERENCE MODE COMPLETE =========="
}

