    # (3) gold.patch - Source-only changes, no test files
    copy_artifact_with_fallback "${zip_root}/reference/gold.patch" "gold.patch" || missing+=("reference/gold.patch (expected: gold.patch)")
    # (4) base_test.patch - Extracted test chunks from ref patch
    copy_artifact_with_fallback "${zip_root}/reference/base_test.patch" "base_test.patch" "test_addition.patch" || missing+=("reference/base_test.patch (expected: base_test.patch or test_addition.patch)")
    # (5) test.patch - Extracted test chunks + cb written test chunks w/ at least 70% diff coverage
    copy_artifact_with_fallback "${zip_root}/reference/test.patch" "test.patch" || missing+=("reference/test.patch (expected: test.patch)")
    # (6) test_files.json - JSON list of all test files modified in test.patch
    copy_artifact_with_fallback "${zip_root}/reference/test_files.json" "test_files.json" || missing+=("reference/test_files.json (expected: test_files.json)")
    # (7) problem_statement.md - Problem statement capturing all high level requests
    copy_artifact_with_fallback "${zip_root}/reference/problem_statement.md" "problem_statement.md" || missing+=("reference/problem_statement.md (expected: problem_statement.md)")
    # (8) interface.txt - Interface provided to the agent
    copy_artifact_with_fallback "${zip_root}/reference/interface.txt" "interface.txt" || missing+=("reference/interface.txt (expected: interface.txt)")
    # (9) combined_prompt.md - Full agent instruction (problem_statement + constraint + interface)
    copy_artifact_with_fallback "${zip_root}/reference/combined_prompt.md" "combined_prompt.md" || missing+=("reference/combined_prompt.md (expected: combined_prompt.md)")
    # (10) proof_coverage.txt - Proof 70% gold.patch coverage using test.patch
    copy_artifact_with_fallback "${zip_root}/reference/proof_coverage.txt" "proof_coverage.txt" || missing+=("reference/proof_coverage.txt (expected: proof_coverage.txt)")
    # (11) comparison_results.json - Docker test results
    copy_artifact_with_fallback "${zip_root}/reference/comparison_results.json" "comparison_results.json" "ref_comparison_results.json" || missing+=("reference/comparison_results.json (expected: comparison_results.json or ref_comparison_results.json)")
    # (12) success.txt - Proof mode succeeded
    copy_artifact_with_fallback "${zip_root}/reference/success.txt" "reference_success.txt" || missing+=("reference/success.txt (expected: reference_success.txt)")
    # (13) rubric.json - JSON containing rubric
    copy_artifact_with_fallback "${zip_root}/reference/rubric.json" "rubric.json" || missing+=("reference/rubric.json (expected: rubric.json)")
    # (14) reference_ratings.png - Proof gold.patch passes rubric 3x in a row (100% pass)
    copy_artifact_with_fallback "${zip_root}/reference/reference_ratings.png" "reference_ratings.png" || missing+=("reference/reference_ratings.png (expected: reference_ratings.png)")
 
    # -------------------------------------------------------------------------
    # agent/ files (7 files)
    # -------------------------------------------------------------------------
    # (15) environment.png - Agent environment setup screenshot
    copy_artifact_with_fallback "${zip_root}/agent/environment.png" "environment.png" || missing+=("agent/environment.png (expected: environment.png)")
    # (16) conversation.md - Agent conversation log
    copy_artifact_with_fallback "${zip_root}/agent/conversation.md" "conversation.md" || missing+=("agent/conversation.md (expected: conversation.md)")
    # (17) agent.patch - Agent-generated refactor solution
    copy_artifact_with_fallback "${zip_root}/agent/agent.patch" "agent.patch" || missing+=("agent/agent.patch (expected: agent.patch)")
    # (18) comparison_results.json - Docker results (agent.patch + test.patch)
    copy_artifact_with_fallback "${zip_root}/agent/comparison_results.json" "agent_comparison_results.json" || missing+=("agent/comparison_results.json (expected: agent_comparison_results.json)")
    # (19) success.txt - Proof mode succeeded
    copy_artifact_with_fallback "${zip_root}/agent/success.txt" "agent_success.txt" || missing+=("agent/success.txt (expected: agent_success.txt)")
    # (20) test_failure_justification.txt - Failing agent test results are not false negatives
    copy_artifact_with_fallback "${zip_root}/agent/test_failure_justification.txt" "test_failure_justification.txt" || missing+=("agent/test_failure_justification.txt (expected: test_failure_justification.txt)")
    # (21) rubric_failure_justification.txt - Failing agent rubric results are not false negatives
    copy_artifact_with_fallback "${zip_root}/agent/rubric_failure_justification.txt" "rubric_failure_justification.txt" || missing+=("agent/rubric_failure_justification.txt (expected: rubric_failure_justification.txt)")
 
    # -------------------------------------------------------------------------
    # e2e/ files (2 files)
    # -------------------------------------------------------------------------
    # (22) validation.txt - combined_mode master validation
    copy_artifact_with_fallback "${zip_root}/e2e/validation.txt" "validation.txt" || missing+=("e2e/validation.txt (expected: validation.txt)")
    # (23) success.txt - Proof mode succeeded
    copy_artifact_with_fallback "${zip_root}/e2e/success.txt" "e2e_success.txt" || missing+=("e2e/success.txt (expected: e2e_success.txt)")
 
    # -------------------------------------------------------------------------
    # scripts/ files (optional, only included if modified)
    # -------------------------------------------------------------------------
    # These scripts are copied from WORKSPACE only if they were modified
    # (compared against initial checksums saved by before_repo_set_cmd.sh)
    local workspace_dir="${WORKSPACE:-.}"
    local scripts_copied=0
    local initial_checksums_file="${ARTIFACT_DIR}/scripts_initial_checksums.txt"
    
    # Function to get current checksum of a file
    get_checksum() {
        local file="$1"
        if command -v md5sum &>/dev/null; then
            md5sum "$file" | awk '{print $1}'
        elif command -v md5 &>/dev/null; then
            md5 -r "$file" | awk '{print $1}'
        fi
    }
    
    # Function to get initial checksum from saved file
    get_initial_checksum() {
        local script_name="$1"
        if [[ -f "$initial_checksums_file" ]]; then
            grep "${script_name}$" "$initial_checksums_file" 2>/dev/null | awk '{print $1}'
        fi
    }
    
    # Check and copy each script if modified
    for script in master_validator_script.sh run_script.sh; do
        local script_path="${workspace_dir}/${script}"
        if [[ -f "$script_path" ]]; then
            local current_checksum
            local initial_checksum
            current_checksum=$(get_checksum "$script_path")
            initial_checksum=$(get_initial_checksum "$script")
            
            if [[ -z "$initial_checksum" ]]; then
                # No initial checksum found (before_repo_set_cmd didn't run or file is new)
                log "  Note: No initial checksum for ${script}, including it"
                cp "$script_path" "${zip_root}/scripts/"
                ((scripts_copied++)) || true
            elif [[ "$current_checksum" != "$initial_checksum" ]]; then
                # File was modified
                cp "$script_path" "${zip_root}/scripts/"
                ((scripts_copied++)) || true
                log "  Included: scripts/${script} (modified)"
            else
                log "  Skipped: scripts/${script} (unchanged)"
            fi
        fi
    done
    
    if [[ $scripts_copied -eq 0 ]]; then
        log "  Note: No modified scripts to include in scripts/"
        # Remove empty scripts directory
        rmdir "${zip_root}/scripts" 2>/dev/null || true
    else
        log "  Included ${scripts_copied} modified script(s) in scripts/"
    fi
 
    if [[ ${#missing[@]} -gt 0 ]]; then
        log "ERROR: Delivery zip creation failed because of missing files:"
        for item in "${missing[@]}"; do
            log "  - ${item}"
        done
        rm -rf "$zip_root"
        return 1
    fi
 
    rm -f "$zip_output"
    (
        cd "$ARTIFACT_DIR"
        zip -rq "$zip_output" "${TASK_ID}_v3.1"
    )
    log "SUCCESS: Delivery zip created at ${zip_output}"
}
 
